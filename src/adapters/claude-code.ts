import * as path from 'path';
import { ContextSlot, Platform } from '../types';
import { BaseAdapter } from './base';

export class ClaudeCodeAdapter extends BaseAdapter {
  platform: Platform = 'claude-code';
  displayName = 'Claude Code';
  outputDir = '.claude';

  getOutputPath(slot: ContextSlot, _projectRoot: string): string {
    switch (slot.type) {
      case 'agent':
        return path.join(this.outputDir, 'agents', `${slot.name}.md`);
      case 'rules':
        return 'CLAUDE.md';
      case 'memory':
        return path.join(this.outputDir, 'memory', `${slot.name}.md`);
      case 'docs':
        return path.join(this.outputDir, 'docs', `${slot.name}.md`);
      default:
        return path.join(this.outputDir, `${slot.name}.md`);
    }
  }

  formatContent(slot: ContextSlot): string {
    const header = `<!-- Generated by ctx - DO NOT EDIT DIRECTLY -->\n<!-- Source: ${slot.sourcePath} -->\n\n`;

    if (slot.type === 'rules') {
      return header + slot.content;
    }

    return header + `# ${slot.name}\n\n${slot.content}`;
  }
}
